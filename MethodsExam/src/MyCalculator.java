public class MyCalculator {

	
	
	int add(int num1, int num2) {
		int sum = 0;
		sum = num1 + num2;
		return sum;		
	}
	
	int subtract(int num1, int num2) {
		int minus = 0;
		minus = num1 - num2;
		return minus;		
	}
	
	int times(int num1, int num2) {
		int times = 0;
		times = num1 * num2;
		return times;
	}
	
	double divide(int num1, double num2) {
		double divide = 0.0;
		divide = (double)num1 / num2;
		return divide;
			
	}
	
	
	
	
}
